#include <libft.h>

/**
 * @brief	Writes the given character to the given file descriptor
 * @param	c	Character to write
 * @param	fd	File descriptor to write to
 */
void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
