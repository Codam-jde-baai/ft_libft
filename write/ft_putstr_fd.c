#include <libft.h>

/**
 * @brief	Writes the given string to the given file descriptor
 * @param	s	String to write
 * @param	fd	File descriptor to write to
 */
void	ft_putstr_fd(char *s, int fd)
{
	unsigned int	i;

	if (s == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
		i++;
	write(fd, s, i);
}
