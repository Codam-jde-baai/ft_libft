#include <libft.h>

/**
 * @brief	Applies the given function to each character of the given string
 * @param	s	String to iterate over
 * @param	f	Function to apply
 */
void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		while (s[i] != '\0')
		{
			f(i, &s[i]);
			i++;
		}
	}
}
