#include <libft.h>

/**
 * @brief	Checks if the given character is alphabetic
 * @param	c	Character to check
 * @return	True if the character is alphabetic,
 * 			false otherwise
 */
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}
