#include <libft.h>

/**
 * @brief	Checks if the given character is ASCII
 * @param	c	Character to check
 * @return	True if the character is ASCII,
 * 			false otherwise
 */
int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	else
		return (0);
}
