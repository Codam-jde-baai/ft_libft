#include <libft.h>

/**
 * @brief	Converts a lowercase letter to uppercase
 * @param	c	Character to convert
 * @return	Converted character
 */
int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
	{
		c = c - 32;
		return (c);
	}
	else
		return (c);
}
