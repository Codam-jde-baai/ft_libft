#include <libft.h>

/**
 * @brief	Checks if the given character is a digit
 * @param	c	Character to check
 * @return	True if the character is a digit,
 * 			false otherwise
 */
int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}
