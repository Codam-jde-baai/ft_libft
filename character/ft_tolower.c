#include <libft.h>

/**
 * @brief	Converts an uppercase letter to lowercase
 * @param	c	Character to convert
 * @return	Converted character
 */
int	ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
	{
		c = c + 32;
		return (c);
	}
	else
		return (c);
}
